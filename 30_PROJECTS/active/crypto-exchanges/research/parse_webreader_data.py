def parse_webreader_table(data: str) -> list[dict]:
    """Парсит таблицу из webReader данных."""
    exchanges = []
    lines = data.strip().split('\n')

    for line in lines:
        # Пропускаем заголовок и разделитель
        if line.startswith('|') and '---' not in line and 'Курсы обмена' not in line:
            parts = [p.strip() for p in line.split('|')]
            # Ожидаем формат: | | Название | Статус | Резервы | ...
            if len(parts) >= 5:
                name = parts[2]
                status = parts[3]
                reserves_raw = parts[4]

                # Пропускаем пустые строки и заголовки
                if not name or name == 'Обменник' or name == 'Image 2: Курсы обмена':
                    continue

                # Парсим сумму резервов
                reserves = 0
                if reserves_raw and reserves_raw != 'Резервы':
                    # Извлекаем числа из резерва
                    numbers = re.findall(r'[\d\s]+', reserves_raw)
                    if numbers:
                        clean = ''.join(numbers).replace(' ', '')
                        try:
                            if clean:
                                reserves = int(clean)
                        except ValueError:
                            pass

                exchanges.append({
                    'name': name,
                    'status': status,
                    'reserves_raw': reserves_raw,
                    'reserves_usd': reserves
                })

    return exchanges


def main():
    """Главная функция."""
    print("Парсинг данных из webReader...")

    # Парсим таблицу
    exchanges = parse_webreader_table(WEBREADER_LIST_DATA)

    print(f"Найдено {len(exchanges)} обменников")

    # Сортируем по убыванию резервов
    sorted_exchanges = sorted(exchanges, key=lambda x: x['reserves_usd'], reverse=True)

    # Выводим топ-10
    print("\n=== Топ-10 обменников по резервам ===")
    for i, ex in enumerate(sorted_exchanges[:10], 1):
        print(f"{i}. {ex['name']}: {ex['reserves_raw']}")

    # Сохраняем в файлы
    output_dir = Path("/Users/epodkin/Вижуалка/executive-assistant/30_PROJECTS/active/crypto-exchanges")
    output_dir.mkdir(parents=True, exist_ok=True)

    # Сохраняем JSON
    result = {
        'timestamp': datetime.now().isoformat(),
        'total_count': len(exchanges),
        'exchanges': sorted_exchanges
    }

    json_path = output_dir / "exchanges_from_webreader.json"
    with open(json_path, 'w', encoding='utf-8') as f:
        json.dump(result, f, ensure_ascii=False, indent=2)
    print(f"\nJSON сохранен: {json_path}")

    # Сохраняем CSV
    csv_path = output_dir / "exchanges_reserves.csv"
    with open(csv_path, 'w', encoding='utf-8') as f:
        f.write("Обменник,Статус,Резервы USD,Резервы исходный\n")
        for ex in sorted_exchanges:
            reserves_formatted = f"{ex['reserves_usd']:,}".replace(',', ' ')
